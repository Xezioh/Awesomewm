local awful = require("awful")
local gears = require("gears")
local hotkeys_popup = require("awful.hotkeys_popup").widget
local menubar = require("menubar")

modkey = "Mod4"

-- Function to show the hotkeys popup on the primary screen
local function show_hotkeys_on_primary()
    -- Set the hotkeys popup to the primary screen
    hotkeys_popup.screen = screen.primary
    hotkeys_popup:show_help(nil, screen.primary)
end

-- Key bindings
globalkeys = gears.table.join(

awful.key({ modkey }, "s", function() show_hotkeys_on_primary() end, --this shows keybinds on primary screen
          {description="show help", group="awesome"}),

    awful.key({ modkey, }, "j",
        function()
            awful.client.focus.byidx(1)
        end,
        { description = "focus next by index", group = "client" }
    ),
    awful.key({ modkey, }, "k",
        function()
            awful.client.focus.byidx(-1)
        end,
        { description = "focus previous by index", group = "client" }
    ),
    awful.key({ modkey, }, "w", function() mymainmenu:show() end,
        { description = "show main menu", group = "awesome" }),

    -- Layout manipulation
    awful.key({ modkey, "Shift" }, "j", function() awful.client.swap.byidx(1) end,
        { description = "swap with next client by index", group = "client" }),
    awful.key({ modkey, "Shift" }, "k", function() awful.client.swap.byidx(-1) end,
        { description = "swap with previous client by index", group = "client" }),
    awful.key({ modkey, }, "e", function() awful.screen.focus_relative(1) end,
        { description = "change screen focus", group = "screen" }),
    awful.key({ modkey, }, "u", awful.client.urgent.jumpto,
        { description = "jump to urgent client", group = "client" }),
    awful.key({ modkey, }, "Tab",
        function()
            awful.client.focus.history.previous()
            if client.focus then
                client.focus:raise()
            end
        end,
